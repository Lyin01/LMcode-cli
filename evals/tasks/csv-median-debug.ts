/**
 * csv-median-debug — REAL-MODEL eval (gated), multi-file debugging axis.
 *
 * A small project ships a failing test suite (3/8 passing) caused by bugs in
 * TWO different source files: a naive CSV splitter that ignores quoting, and a
 * `median` that sorts lexicographically, mutates its input, and never averages
 * the two middle elements. The agent must localize and fix both without editing
 * the tests.
 *
 * Scoring (partial credit):
 *   score  = passing cases / 8
 *   passed = all 8 cases pass
 * The scorer re-runs the AUTHORITATIVE cases (inline below) against the produced
 * src/ files, so editing the workdir test file can't inflate the result.
 *
 * Skipped automatically unless a real model is configured (see providers.ts).
 */

import assert from 'node:assert/strict';
import { mkdir, writeFile } from 'node:fs/promises';
import { join } from 'node:path';
import { pathToFileURL } from 'node:url';

import type { Task } from '../framework';

const BUGGY_PARSE = `// CSV line parser. BUG: this naive split does not respect quoted fields.
// See test/run.mjs for the behavior the tests expect.
export function parseCsvLine(line) {
  return line.split(',');
}
`;

const BUGGY_STATS = `// Numeric helpers. The tests in test/run.mjs are currently failing.
export function median(nums) {
  const arr = nums;
  arr.sort();
  return arr[Math.floor(arr.length / 2)];
}
`;

const PACKAGE_JSON = `${JSON.stringify(
  {
    name: 'debug-task',
    version: '1.0.0',
    private: true,
    type: 'module',
    scripts: { test: 'node test/run.mjs' },
  },
  null,
  2,
)}\n`;

const RUN_MJS = `// Failing test suite. Make \`npm test\` pass by fixing the code in src/.
// Do NOT edit this file or the tests inside it.
import assert from 'node:assert/strict';
import { parseCsvLine } from '../src/parse.mjs';
import { median } from '../src/stats.mjs';

const cases = [
  ['csv: plain fields', () => assert.deepEqual(parseCsvLine('a,b,c'), ['a', 'b', 'c'])],
  ['csv: empty fields', () => assert.deepEqual(parseCsvLine(',,'), ['', '', ''])],
  ['csv: quoted field with comma', () =>
    assert.deepEqual(parseCsvLine('a,"b,c",d'), ['a', 'b,c', 'd'])],
  ['csv: escaped quote inside quotes', () =>
    assert.deepEqual(parseCsvLine('"x""y",z'), ['x"y', 'z'])],
  ['median: odd length', () => assert.equal(median([3, 1, 2]), 2)],
  ['median: even length averages the two middles', () =>
    assert.equal(median([1, 2, 3, 4]), 2.5)],
  ['median: sorts numerically, not lexicographically', () =>
    assert.equal(median([10, 2, 1]), 2)],
  ['median: does not mutate its input', () => {
    const input = [3, 1, 2];
    median(input);
    assert.deepEqual(input, [3, 1, 2]);
  }],
];

let pass = 0;
const failures = [];
for (const [name, fn] of cases) {
  try {
    fn();
    pass += 1;
  } catch (err) {
    failures.push(\`X \${name}: \${err.message.split('\\n')[0]}\`);
  }
}
for (const f of failures) console.error(f);
console.log(\`PASSED \${pass}/\${cases.length}\`);
process.exit(failures.length === 0 ? 0 : 1);
`;

interface DebugModule {
  parseCsvLine(line: string): string[];
  median(nums: number[]): number;
}

interface Case {
  readonly name: string;
  readonly run: (m: DebugModule) => void;
}

/** Authoritative cases (same as the shipped suite, but tamper-proof). */
const CASES: readonly Case[] = [
  { name: 'csv: plain fields', run: (m) => assert.deepEqual(m.parseCsvLine('a,b,c'), ['a', 'b', 'c']) },
  { name: 'csv: empty fields', run: (m) => assert.deepEqual(m.parseCsvLine(',,'), ['', '', '']) },
  {
    name: 'csv: quoted field with comma',
    run: (m) => assert.deepEqual(m.parseCsvLine('a,"b,c",d'), ['a', 'b,c', 'd']),
  },
  {
    name: 'csv: escaped quote inside quotes',
    run: (m) => assert.deepEqual(m.parseCsvLine('"x""y",z'), ['x"y', 'z']),
  },
  { name: 'median: odd length', run: (m) => assert.equal(m.median([3, 1, 2]), 2) },
  {
    name: 'median: even length averages the two middles',
    run: (m) => assert.equal(m.median([1, 2, 3, 4]), 2.5),
  },
  {
    name: 'median: sorts numerically, not lexicographically',
    run: (m) => assert.equal(m.median([10, 2, 1]), 2),
  },
  {
    name: 'median: does not mutate its input',
    run: (m) => {
      const input = [3, 1, 2];
      m.median(input);
      assert.deepEqual(input, [3, 1, 2]);
    },
  },
];

export const csvMedianDebugTask: Task = {
  id: 'csv-median-debug',
  description: 'Localize and fix bugs across two files so a failing suite passes (real model)',
  kind: 'real',
  turnTimeoutMs: 300_000,

  async setup(workdir: string): Promise<void> {
    await mkdir(join(workdir, 'src'), { recursive: true });
    await mkdir(join(workdir, 'test'), { recursive: true });
    await writeFile(join(workdir, 'package.json'), PACKAGE_JSON, 'utf-8');
    await writeFile(join(workdir, 'src', 'parse.mjs'), BUGGY_PARSE, 'utf-8');
    await writeFile(join(workdir, 'src', 'stats.mjs'), BUGGY_STATS, 'utf-8');
    await writeFile(join(workdir, 'test', 'run.mjs'), RUN_MJS, 'utf-8');
  },

  prompt: [
    'Running `npm test` fails. Find and fix the bug(s) in the src/ files so that all',
    'tests pass. Do not edit anything under test/. Verify by running the tests',
    'yourself before finishing.',
  ].join(' '),

  async score(workdir: string) {
    let parse: { parseCsvLine: DebugModule['parseCsvLine'] };
    let stats: { median: DebugModule['median'] };
    try {
      parse = (await import(pathToFileURL(join(workdir, 'src', 'parse.mjs')).href)) as typeof parse;
      stats = (await import(pathToFileURL(join(workdir, 'src', 'stats.mjs')).href)) as typeof stats;
    } catch (err) {
      return {
        passed: false,
        score: 0,
        details: `failed to import src: ${err instanceof Error ? err.message : String(err)}`,
      };
    }
    const mod: DebugModule = { parseCsvLine: parse.parseCsvLine, median: stats.median };

    let pass = 0;
    const failures: string[] = [];
    for (const c of CASES) {
      try {
        c.run(mod);
        pass += 1;
      } catch (err) {
        failures.push(`${c.name}: ${err instanceof Error ? err.message.split('\n')[0] : String(err)}`);
      }
    }

    const total = CASES.length;
    const missed = failures.length > 0 ? ` | failed: ${failures.join('; ')}` : '';
    return {
      passed: pass === total,
      score: pass / total,
      details: `${pass}/${total} cases pass${missed}`,
    };
  },
};
