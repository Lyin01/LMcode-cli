/**
 * fix-failing-fn — REAL-MODEL eval (gated).
 *
 * Sets up a tiny dependency-free project with a deliberately buggy function and
 * a check script that fails against it. The agent is asked to make the check
 * pass. The scorer runs the check with `node` and passes iff it exits 0.
 *
 * This is skipped automatically unless a real model is configured (see
 * `resolveRealModel` / evals/README.md). A fake model cannot pass it.
 *
 * Fixture (all plain ESM, no deps so the scorer needs only `node`):
 *   src/sum.mjs    — `export function sum(a, b) { return a - b; }`  (BUG: subtracts)
 *   check.mjs      — asserts sum(2,3)===5 and sum(-1,1)===0; throws + exits 1 on failure
 */

import { spawn } from 'node:child_process';
import { mkdir, writeFile } from 'node:fs/promises';
import { join } from 'node:path';

import type { Task } from '../framework';

const BUGGY_SUM = `// A deliberately buggy implementation. It should ADD its two arguments.
export function sum(a, b) {
  return a - b;
}
`;

const CHECK_SCRIPT = `// Dependency-free check: exits 0 on success, throws (exit 1) on failure.
// Do not edit this file — the eval scorer runs it as-is.
import { sum } from './src/sum.mjs';

function assertEqual(actual, expected, label) {
  if (actual !== expected) {
    throw new Error(\`\${label}: expected \${expected}, got \${actual}\`);
  }
}

assertEqual(sum(2, 3), 5, 'sum(2, 3)');
assertEqual(sum(-1, 1), 0, 'sum(-1, 1)');
assertEqual(sum(0, 0), 0, 'sum(0, 0)');
console.log('OK');
`;

function runNode(workdir: string, script: string): Promise<{ code: number; output: string }> {
  return new Promise((resolve) => {
    const child = spawn(process.execPath, [script], {
      cwd: workdir,
      stdio: ['ignore', 'pipe', 'pipe'],
    });
    let output = '';
    child.stdout.on('data', (d) => (output += String(d)));
    child.stderr.on('data', (d) => (output += String(d)));
    child.on('error', (err) => resolve({ code: 1, output: String(err) }));
    child.on('close', (code) => resolve({ code: code ?? 1, output: output.trim() }));
  });
}

export const fixFailingFnTask: Task = {
  id: 'fix-failing-fn',
  description: 'Agent fixes a buggy function so the project check passes (real model)',
  kind: 'real',

  async setup(workdir: string): Promise<void> {
    await mkdir(join(workdir, 'src'), { recursive: true });
    await writeFile(join(workdir, 'src', 'sum.mjs'), BUGGY_SUM, 'utf-8');
    await writeFile(join(workdir, 'check.mjs'), CHECK_SCRIPT, 'utf-8');
  },

  prompt: [
    'The file src/sum.mjs has a bug: its sum() function should add its two',
    'arguments but currently does not. Fix src/sum.mjs so the project check passes.',
    'You can verify with: node check.mjs (it prints OK and exits 0 when fixed).',
    'Do not modify check.mjs.',
  ].join(' '),

  async score(workdir: string) {
    const { code, output } = await runNode(workdir, 'check.mjs');
    if (code === 0) {
      return { passed: true, score: 1, details: `check.mjs passed: ${output}` };
    }
    return {
      passed: false,
      score: 0,
      details: `check.mjs failed (exit ${code}): ${output || '<no output>'}`,
    };
  },
};
